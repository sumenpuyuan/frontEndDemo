<script type="text/javascript">
	/*val d_Value=$(".search input.text").val();//获取默认值，输入关键字
	alert(d_Value);
	$(".search input.text").focus(function(){
		
	});*/
	var a=$(".search input.text").val();//获取默认关键字
	//alert(a);
	//下面是得到焦点时
	$(".search input.text").focus(function(){
		//$(this)等价于".search input.text"
		if($(this).val()==a)
		{
			$(this).val("");//设置为空
		}
	});
	//下面是失去焦点时
	$("$.search input.text").blur(
		function()
		{
			if($(this).val()=="")
			{
				$(this).val(a);
			}
		}
	);
	//下面是二级菜单
	$(".Nav .MainNav ul li").hover(function(){
		$(this).find(".menu").show();//显示
		$(this).addClass("hover");//给li添加hover属性
	},
	function(){
		$(this).find(".menu").hide();//隐藏
		$(this).removeClass("hover");//移除
	})
	/*广告轮播图*/
	/*鼠标移动到哪儿就转换图片*/
	var _index=0;//初始化序列号
	var timePlay=null;//初始化定时器
	$("ul.button li").hover(function(){/*鼠标在上面*/
		clearInterval(timePlay);//当鼠标特效与移动特效同时发生时，优先鼠标，
		_index=$(this).index();//获取当前li序列号
		//alert(_index);//弹窗
		$(this).addClass("hover").siblings().removeClass("hover");//显示按扭
		//fadein淡入，fadeout淡出
		$("#Adv .ImageList").eq(_index).fadeIn().siblings("div").fadeOut();//隐藏同等级的元素，会出现把按钮隐藏的问题，所以要隐藏同等级的div元素
	},
	function(){
		autoPlay();
		//鼠标移开时重新开启播放器
	}
	);
	function autoPlay(){
		//alert("sss");
		//设置定时器
		timePlay=setInterval(function(){
			_index++;
			if(_index<4){
				$("ul.button li").eq(_index).addClass("hover").siblings().removeClass("hover");//显示按扭
				$("#Adv .ImageList").eq(_index).fadeIn().siblings("div").fadeOut();//显示图片
				//alert(_index);
			}else{_index=-1;/*设置序列号为-1,跳到播放第一张图片*/}
		},2000);	
	};
	autoPlay();//调用和执行
</script>
